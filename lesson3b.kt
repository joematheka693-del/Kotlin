fun main() {
    // looping from a  higher number to a lower number by us of a for loop
    // create a program that is able to print from 10 to 1
    for (i in 10 downTo 1) {
        println(i)
    }

    println("=======================================")
    // Create a program that find odd numbers from 20 t0 10
    for (x in 19 downTo 10 step 2) {
        println(x)
    }

}