fun main() {
    // Below we declare variables and then we state their data types
    // To avoid data type mismatch we specify the data types
    // And to save on memory
    val number1 : Int = 5
    val number2 : Double = 3.142
    val letter : Char = 'B'
    val paidLoan : Boolean = false
    val name : String = "James"



    // Print out the output
    println(number1)
    println(number2)
    println(letter)
    println(paidLoan)
    println(name)
}

