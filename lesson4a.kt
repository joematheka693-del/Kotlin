fun main() {
    // Functions are blocks of statements that perform a specific task.
    // A function is introduced with the fun keyword, followed by the function name, a list of parameters in parentheses, and a body enclosed in curly braces.
    fun sayHello () {
        println("Hello there, Hope you are having a great day!")
    }
    sayHello() // Calling the function to execute its code

    println("===============================================================")

    // Create your own example of a function below and also invoke it
    fun greetUser() {
        println("Hello, Welcome to Kotlin programming.")
    }
    greetUser() // Calling the function with an argument
}