fun main() {
    // While loop
    // Create a program able to print from 0 to 10

    var count = 0

    while (count <= 10) {
        println("The number is: $count")
        count = count + 1
    }

    println("==========================================")
    // Create a while loop printing fro 50 to 35 with the steps of 2
    var x = 50

    while (x >= 35) {
        println(x)
        x -= 2
    }
}